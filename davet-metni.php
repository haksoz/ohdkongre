    <p><b>Değerli Meslektaşlarımız,</b></p>
    <p>
        Onkoloji Hemşireliği Derneği olarak, 35 yıllık birikim ve deneyimimizle, meslektaşlarımızın değerli katkıları ve
        yoğun talepleri doğrultusunda ilk uluslararası kongremizi düzenlemenin onurunu ve gururunu yaşıyoruz .
        <b>I. Uluslararası Onkoloji Hemşireliği Derneği Kongresi</b>’ni, 21-23 Mayıs 2025 tarihleri arasında Gazi
        Üniversitesi
        Hemşirelik Fakültesi ile iş birliği içinde, Ankara’da gerçekleştireceğiz. Sizleri bu tarihi etkinliğe davet
        etmekten büyük mutluluk duyuyoruz.
    </p>
    <p>
        Onkoloji hemşireleri birey, aile ve toplumun onkoloji alanında farkındalığını artırarak ve kaliteli bakım
        hizmeti
        sunarak toplum sağlığının korunması ve yükseltilmesinde önemli bir rol oynar. Bu bağlamda kongrenin ana teması
        “Onkoloji Hemşireliğinde 35 Yıllık Miras: Geçmişten Geleceğe” olarak belirlenmiştir. Kongremiz, onkoloji
        hemşireliğine yön veren ulusal ve uluslararası düzeyde saygın bilim insanlarının, uzmanların ve klinik
        uygulayıcıların bir araya geleceği eşsiz bir platform olacaktır. Onkoloji hemşireliği uygulamasında Kanserden
        Korunma ve Erken Tanı: Farkındalık ve Hayat Kurtaran Stratejiler; Kanser Bakımında Yenilikçi ve Sürdürülebilir
        Hemşirelik Yaklaşımları; Teknolojik Gelişmelerin Onkoloji Hemşireliğine Yansımaları, Kanserle Yaşamak: Yeni Bir
        Denge ve Onkoloji Hemşireliğinde Yaşanan Sorunlar ve Çözüm Önerileri gibi alanlarda bilgi ve deneyim
        paylaşımının yapılacağı bu kongre, alanımızda geleceği şekillendiren tartışmalara ev sahipliği yapacaktır.
    </p>
    <p>
        Bu tarihi etkinlikte sizlerin değerli katkıları, bilgi birikiminiz ve katılımınızla kongremizin daha da
        zenginleşeceğine inanıyoruz. Hep birlikte onkoloji hemşireliği alanında yeni ufuklar açacağımız bu önemli
        buluşmada sizleri aramızda görmekten büyük mutluluk duyacağız.
    </p>
