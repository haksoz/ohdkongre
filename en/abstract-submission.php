                            <li>Words should be separated by commas and written in alphabetical order.</li>
                            <li>Turkish keywords should be provided according to the Turkish Scientific Terms (TBT). English keywords should follow <a href="www.nlm.nih.gov/mesh/MBrowser.html" target="_blank">Medical Subject Headings (MeSH)</a>.</li>
                        </ul>
                    </li>
                </ul>
