                                <h3>3. Abstract Submission Rules (Format)</h3>
                                <ol>
                                    <li>Abstracts must be prepared in both <b>Turkish and English</b> and uploaded to the system.</li>
                                    <li>Each abstract (excluding title, author names, affiliations, and keywords) must not exceed <b>400 words</b>.</li>
                                    <li>Abstracts must be written in <b>Times New Roman</b>, <b>11-point</b> font size, with <b>1.5 line spacing</b>.</li>
                                </ol>
                                
                                <h4>Abstract Title:</h4>
                                <ul>
                                    <li>Must reflect the study content, be concise, and contain no abbreviations.</li>
                                    <li>Only the first letter is capitalized for Turkish titles; for English titles, prepositions such as "on" and "of" should be in lowercase.</li>
                                </ul>

                                <h4>Author Names:</h4>
                                <ul>
                                    <li>Academic titles should not be used.</li>
                                    <li>Only the first letter of names should be capitalized.</li>
                                    <li>Full institutional information must be provided (university, faculty/department, city, country).</li>
                                </ul>

                                <h4>Abbreviations:</h4>
                                <ul>
                                    <li>If abbreviations are used, they must be explained in parentheses the first time they appear.</li>
                                </ul>

                                <h4>Required Abstract Sections:</h4>
                                <ul>
                                    <li><strong>Original Research:</strong> Introduction and Aim, Method, Results, Conclusion</li>
                                    <li><strong>Case Report:</strong> Introduction and Aim, Case(s), Conclusion</li>
                                    <li><strong>Review:</strong> The aim and content should be presented as plain text.</li>
                                </ul>

                                <h4>Keywords:</h4>
                                <ul>
                                    <li>A minimum of <b>3</b> and a maximum of <b>5 keywords</b> should be provided.</li>
                                    <li>Keywords must be listed alphabetically and separated by commas.</li>
                                    <li>Turkish keywords should comply with Türkiye Bilim Terimleri (TBT).</li>
                                    <li>English keywords should comply with Medical Subject Headings (MeSH).</li>
                                </ul>

                                <h3>4. Full Text Submission Rules</h3>
                                <ol>
                                    <li>Full text submission is not mandatory. Participation is possible with abstract-only submissions.</li>
                                    <li>Full texts can be uploaded after abstract acceptance results are announced. Participants with rejected abstracts may not submit full texts.</li>
                                </ol>

                                <h4>Full Text Format:</h4>
                                <ul>
                                    <li>May be prepared in <b>Turkish or English</b>.</li>
                                    <li>Must be between <b>1500–2500 words</b> (excluding title, author information, and references).</li>
                                    <li>Written in <b>Times New Roman</b>, <b>11-point</b>, <b>1.5 line spacing</b>.</li>
                                    <li>All tables, figures, and graphics must be titled and placed according to their order of appearance.</li>
                                    <li>Figures must be digital, JPEG format, and 300 dpi resolution.</li>
                                </ul>

                                <h4>Full Text Sections:</h4>
                                <ul>
                                    <li><strong>Original Research:</strong> Abstract (Turkish and English), Introduction and Aim, Method, Results, Discussion, Conclusion, References</li>
                                    <li><strong>Case Report:</strong> Abstract (Turkish and English), Introduction and Aim, Case(s), Discussion, Conclusion, References</li>
                                    <li><strong>Review:</strong> Abstract (Turkish and English), Introduction and Aim, subheadings if any, Conclusion, References</li>
                                </ul>

                                <h4>Keywords:</h4>
                                <ul>
                                    <li>Minimum <b>3</b>, maximum <b>5</b>.</li>
                                    <li>Listed alphabetically and separated by commas.</li>
                                    <li>Turkish keywords must follow TBT; English keywords must follow MeSH.</li>
                                    <li>References must follow <b>APA 7 style</b>.</li>
                                </ul>

                                <h3>5. Evaluation of Abstracts</h3>
                                <ol>
                                    <li>Abstracts will be evaluated anonymously by independent reviewers.</li>
                                    <li>Based on reviewer reports, abstracts may be accepted, rejected, or returned for revision.</li>
                                    <li>Acceptance or rejection notifications will be sent to the corresponding author via e-mail.</li>
                                </ol>

                                <h3>6. Presentation of Abstracts</h3>
                                
                                <h4>Oral Presentation:</h4>
                                <ol>
                                    <li>Oral presentations may be delivered in Turkish or English.</li>
                                    <li>Presentation duration is limited to <b>5 minutes</b> including discussion.</li>
                                    <li>Presentations must be delivered at the specified date, time, and hall in the scientific program. Failure to present results in exclusion from the abstract book.</li>
                                    <li>An abstract cannot be presented on behalf of another author; the presenter must be an author or co-author.</li>
                                </ol>

                                <h4>Poster Presentation:</h4>
                                <ol>
                                    <li>Posters must be prepared as e-posters, sized <b>70 cm x 90 cm</b>.</li>
                                    <li>Posters must be uploaded as PDF files.</li>
                                    <li>Poster content sections:
                                        <ul>
                                            <li><strong>Original Research:</strong> Introduction and Aim, Method, Results, Discussion, Conclusion, References</li>
                                            <li><strong>Case Report:</strong> Introduction and Aim, Case(s), Discussion, Conclusion, References</li>
                                            <li><strong>Review:</strong> Introduction and Aim, subheadings if any, Conclusion, References</li>
                                        </ul>
                                    </li>
                                    <li>Posters must be displayed in the designated area throughout the congress.</li>
                                </ol>

                                <h3>7. Important Dates</h3>
                                <p class="text-start"><strong>Abstract Submission Deadline:</strong> April 12, 2026</p>

                                <h3>8. Publication of Abstracts</h3>
                                <ol>
                                    <li>All abstracts presented at the congress will be published electronically in the congress abstract book.</li>
                                    <li>Abstracts that are not presented will not be included in the book.</li>
                                    <li>Authors bear full responsibility for ethical approval, accuracy of references, and compliance with language rules.</li>
