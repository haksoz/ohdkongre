                                    <li>Abstracts sent by e-mail, fax, or postal mail will not be accepted.</li>
                                    <li>Before submitting an abstract, authors must register in the online system. If you do not have an existing account, you may create a new one through the platform.</li>
                                    <li>Each participant may submit a maximum of <b>two abstracts</b>. If more than two abstracts are submitted, a fee of 1000 TL will be charged for each additional abstract.</li>
                                    <li>Abstracts will be published exactly as submitted; therefore, authors are responsible for checking all spelling and formatting.</li>
                                    <li>If an abstract is accepted, the presenting author must complete congress registration. Otherwise, the abstract will not be presented or included in the congress abstract book.</li>
