                <h4>Abstract Submission Guidelines</h4>
                <!-- Custom List -->
                <ul class="custom no-margin" style="font-weight:normal;">
                    <li> Abstracts should be prepared and uploaded in both <b>Turkish and English</b>.</li>
                    <li> Each abstract in both languages (excluding title, author name, surname, institutional information, and keywords) should not exceed <b>400 words</b>.</li>
                    <li> Abstracts should be prepared using <b>Times New Roman</b> font, <b>11-point</b> size, and <b>1.5 line spacing</b>.</li>
                    <li> Abstract title:
                        <ul>
                            <li>Should reflect the content of the study, be short and concise, and should not contain abbreviations.</li>
                            <li>In Turkish titles, only the first letter should be capitalized, while in English titles, prepositions like "on, of" should be written in lowercase.</li>
                        </ul>
                    </li>
                    <li> Academic titles should not be used in author names. Names should be written with only the first letter capitalized.</li>
                    <li> The names and addresses of the institutions where the authors work (university name, faculty/department, city, and country) must be provided.</li>
                    <li> If abbreviations are used in the abstract, the abbreviation should be explained in parentheses at the first occurrence.</li>
                    <li> Abstracts should include the following sections:
                        <ul>
                            <li><b>Original Research:</b> Introduction and Purpose, Method, Results, Conclusion</li>
                            <li><b>Case Report:</b> Introduction and Purpose, Case(s), Conclusion</li>
                            <li><b>Review:</b> The purpose and content of the review should be stated as plain text.</li>
                        </ul>
                    </li>
                    <span class="text-red">* </span>Case reports and reviews will only be accepted as poster presentations at the congress.
                    <li> Keywords:
