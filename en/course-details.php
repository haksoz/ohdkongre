        <div class="alert alert-info m-0">
            <h5 class="m-0 p-0">Palliative Care Symptom Management Courses</h5>
            <br>May 21, 2025 - 09:00-17:30
        </div>
        <div class="mb-3"></div>
        <p>
            <b>Purpose and impotance of the course:</b> It is important to gain management skills for common symptoms in
            palliative care. The aim of palliative care is to relieve symptoms such as pain, dyspnea, delirium,
            anorexia-cachexia, nausea, vomiting, constipation, fatigue, weakness, insomnia, anxiety, depression and
            intestinal obstruction, and to improve the patient's quality of life. Management of pain and other
            symptoms is an integral, important component of palliative care and an individualized care plan.
            Therefore, the aim of this course is to share current information on the management of common symptoms
            in palliative care based on evidence-based nursing interventions.
        </p>
