    <p><b>Dear Colleagues,</b></p>
    <p>
        As the Oncology Nursing Association, with 35 years of accumulated knowledge and experience, we are honored and proud to organize our first international congress based on the valuable contributions and high demand from our colleagues.
        We will hold the <b>1st International Oncology Nursing Association Congress</b> on May 21–23, 2025, in collaboration with the Gazi University Faculty of Nursing in Ankara. We are delighted to invite you to this historic event.
    </p>
    <p>
        Oncology nurses play a vital role in enhancing public health by raising awareness in oncology for individuals, families, and society, and by providing quality care services. In this context, the main theme of the congress has been determined as
        "35 Years of Legacy in Oncology Nursing: From Past to Future." Our congress will be a unique platform where respected national and international scientists, experts, and clinical practitioners shaping the field of oncology nursing come together.
        Topics such as Prevention and Early Detection of Cancer: Awareness and Life-Saving Strategies; Innovative and Sustainable Nursing Approaches in Cancer Care; The Impact of Technological Developments on Oncology Nursing; Living with Cancer: A New Balance;
        and Challenges and Solutions in Oncology Nursing will be addressed, facilitating the sharing of knowledge and experiences. This congress will host discussions that shape the future of our field.
    </p>
    <p>
        We believe that your valuable contributions, expertise, and participation will further enrich our congress. We would be delighted to see you among us at this important meeting where we will open new horizons together in oncology nursing.
    </p>
    <p>Forward together! <br>
        Looking forward to seeing you at our congress,
    </p>
